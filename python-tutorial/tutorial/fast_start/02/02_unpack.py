# Распаковка структур данных

t = [1, 2, 3, 4]
a, b = [1, 2]

print(t, sep='*')  # [1, 2, 3, 4]
print(*t, sep='*')  # 1*2*3*4

# добавялем в словарь другой словарь с распаковуой его значений
d1 = {'one': 1, 'two': 2}
d2 = {**d1, 'tree': 3}

print(d1)
print(d2)

a, *b, c = 1, 2, 3, 4, 5
print(a, b, c)  # 1 [2, 3, 4] 5
